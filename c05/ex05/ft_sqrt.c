int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb > 0)
	{
		while (i * i != nb && i <= nb && i < 50000)
			i++;
		if (nb / i == i)
			return (i);
		else
			return (0);
	}
	return (0);
}
