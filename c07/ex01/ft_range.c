#include <stdlib.h>
#include <stdio.h>
#include <unistd.h>

int	*ft_range(int min, int max)
{
	int	*tab;
	int	i;

	i = 0;
	if (min >= max)
		return (NULL);
	tab = (int *)(malloc(sizeof(int) * (max - min)));
	while (tab[i] <= max)
	{
		tab[i] = min;
		i++;
		min++;
	}
	return (tab);
}
